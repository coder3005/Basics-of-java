package TypeCasting;

import java.util.*;

public class typecasting1 {
    public static void main(String args[]) {
        Scanner sc=new Scanner(System.in);
        char ch='a';
        char ch2='b';
        int number=ch;
        int number2=ch2; 
        System.out.println(number);
        System.out.println(number2);
        sc.close();
    }
}
