package Basics;
import java.util.*;

public class javabasics {
    public static void main(String[] args) {
        boolean[] bool=new boolean[5];
        int[] num=new int[10];
        char[] ch=new char[10];
        float[] fl=new float[10];

        System.out.println("Bool : "+ Arrays.toString(bool));
        System.out.println("Num : "+ Arrays.toString(num));
        System.out.println("Char : "+ Arrays.toString(ch));
        System.out.println("Float : "+ Arrays.toString(fl));
    }
}
