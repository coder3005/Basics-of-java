package Basics;

public class unaryincrement {
    public static void main(String arsgs[]) {
        int a=10;
        int b=++a; // pre increment
        int c=a++; // post increment
        int d=15;
        int e=d++;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(e);
    }
}
