package Basics;

public class unarydecrement {
    public static void main(String arsgs[]) {
        int a=10;
        int b=--a; // pre decrement
        int c=a--; // post decrement
        int d=15;
        int e=d--;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(e);
    }
}
